/** Introdução a Javascript - Variáveis
 * 
 * Variáveis são espaçados de memória do computador para armazenar diferentes tipos de dados
 * 
 * Variáveis em JS podem ser de três tipos:
 * var: tipo de variável que pode ser acessado em qualquer parte do código
 * let: tipo de variável que só pode ser acessado dentro so bloco de código que foi declarado
 * const: tipo de variável que só pode ser acessado dentro so bloco de código que foi declarado, e seu valor não pode ser alterado após ter sido declarado
 *
 * Sintaxe:
 * var nomeVariavel = "valor";
 * 
 * Para exibir dados no terminal é utilizado o comando 'console.log(conteudo)'
 * 
 * Concatenação (+): É utilizada para realizar a junção entre trechos textuais
 * 
 * */


