/** Introdução a Javascript - Estruturas de repeticao FOR
 * 
 * Os ciclos permitem que instruções sejam repetidas de maneira simples. 
 * 
 * Sintaxe:
 * for (inicio; condicao; modificador){
 *     //codigo que será executado em cada repeticao
 * }
 * 
 */

 //Exemplo (Dar 5 voltas)

 for (var i = 0; i < 5; i++){
    console.log("Dando a volta de número " + i);
 }
